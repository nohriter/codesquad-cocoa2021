package week1.day3.indian_naming;

public class PlayerInfo {
    private String year;
    private String month;
    private String day;

    public PlayerInfo(String year, String month, String day) {
        this.year = year;
        this.month = month;
        this.day = day;
    }

    public String getYear() {
        return year;
    }

    public String getMonth() {
        return month;
    }

    public String getDay() {
        return day;
    }
}
